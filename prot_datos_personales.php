  <h3>Política de protección de datos personales</h3><br>
  
  <h5>1. Nuestro compromiso con la privacidad</h5>
    En Heavy Meta Productos de Ferretería S.A. (en adelante, "Heavy Meta"), ubicada en Cra. 30 #17B-25Sur Bogotá D.C., valoramos y respetamos la privacidad de todos aquellos que utilizan nuestra página web (en adelante, los "Usuarios"). El presente Aviso de Privacidad tiene como objetivo informar acerca de la recopilación y el uso de la información por parte de Heavy Meta, así como detallar las medidas de seguridad implementadas para proteger los datos de los Usuarios. Asimismo, se indica a quién pueden dirigirse los Usuarios para plantear preguntas o resolver cualquier inquietud relacionada con esta política de privacidad. Cabe destacar que este Aviso de Privacidad se ajusta a los requisitos establecidos por la regulación colombiana vigente.  <br> 

    <br><h5>2. Información personal</h5>
    Al utilizar la página de Heavy Meta (en adelante, la "Página"), los Usuarios comprenden y aceptan que la información recopilada incluirá aquellos datos necesarios para el registro en la Plataforma, así como para llevar a cabo compras y entregas de pedidos. En el momento en que los Usuarios proporcionen datos personales, manifiestan que la información suministrada a través de la Plataforma es precisa, veraz y correcta. Al utilizar la Plataforma y proporcionar información personal, los Usuarios otorgan su consentimiento libre, expreso e informado para que dicha información sea utilizada con fines específicos.   <br>

    <br><h5>3. Recopilación y utilización de su información</h5>
    Heavy Meta solo recopilará información proporcionada voluntariamente por los Usuarios. Si los Usuarios no desean que se recopile Información Personal, simplemente deben abstenerse de proporcionarla. Nos comprometemos a garantizar la confidencialidad de los datos personales de los Usuarios registrados a través de los formularios correspondientes. En caso de que los Usuarios hayan proporcionado voluntariamente datos personales en dichos formularios, podremos realizar el tratamiento interno de esa información.
    La información personal de los Usuarios solo se utilizará para los fines establecidos en la autorización de tratamiento de datos personales, en los Términos y Condiciones de Uso de la Página, y en este Aviso de Privacidad. Además, en caso de ser requerido por orden judicial o por una autoridad competente, estaremos obligados a entregar la información solicitada.
      <br>

      <br><h5>4. Protegiendo su información</h5>
    Para prevenir acceso no autorizado, mantener la precisión de los datos y asegurar el uso correcto de la información, Heavy Meta ha puesto en uso ciertos medios físicos, electrónicos, administrativos y procedimientos de seguridad para resguardar y asegurar la información que recopilamos en línea. Heavy Meta resguarda la información de acuerdo con estándares y procedimientos de seguridad establecidos y continuamente se evalúan nuevas tecnologías para proteger la información. Sin embargo, los Usuarios reconocen que los medios técnicos existentes que brindan seguridad no son inexpugnables, y que aun cuando se adopten todos los recaudos razonables de seguridad es posible sufrir manipulaciones, destrucción y/o pérdida de información.

    Por otro lado, mientras intentamos proteger la Información Personal de los Usuarios, los Usuarios también deberán tomar medidas para proteger su información. Heavy Meta reitera a los Usuarios que deben tomar toda precaución para proteger su información personal mientras están en Internet. Como mínimo, se aconseja el cambio de la clave de acceso a la Página periódicamente, usando una combinación de letras y números, y asegurando de que está utilizando un navegador seguro.
      <br>

      <br><h5>5. Datos de menores de edad</h5>
    Nuestra Página no está dirigida a menores de edad, por lo tanto, en ningún momento se recopilará información de personas menores de edad. Si en algún caso se detecta que se ha obtenido información de un menor de edad, procederemos a eliminarla de manera inmediata. Nos tomamos muy en serio la protección de la privacidad de los menores y nos comprometemos a garantizar que no se recopile ni se almacene información personal de ellos en nuestra plataforma.  <br>

    <br><h5>6. Links externos</h5>
    Algunos sitios web y aplicaciones de Heavy Meta contienen enlaces hacia y desde otros sitios de Internet. Heavy Meta no se hace responsable de las prácticas de privacidad de esos sitios, a menos que estén cubiertos por el apartado "Recopilación y Utilización de su Información" de este Aviso de Privacidad y sean propiedad de HEAVY META PRODUCTOS DE FERRETERÍA S.A. Te recomendamos que investigues las políticas de privacidad de esos sitios. Si decides establecer una relación con los distribuidores de productos de Heavy Meta y compartes tu información personal con ellos, es posible que el distribuidor requiera tu autorización para el tratamiento de datos.  

   <br> <br><h5>7. Derechos del Usuario</h5>
    Según la legislación vigente, los Usuarios tienen derecho a acceder, rectificar o eliminar su Información Personal, así como a ejercer cualquier otro derecho. Para ello, pueden ponerse en contacto con nosotros a través de la línea [número de contacto]. El titular de los datos personales tiene el derecho de acceder a su información de forma gratuita, como establece la ley. En Heavy Meta nos esforzamos por atender todas las solicitudes de manera oportuna y razonable.  <br>

    <br><h5>8. Cambios a esta Política de tratamiento de información</h5>
    Heavy Meta se reserva el derecho de realizar modificaciones a esta Política. Es responsabilidad de los Usuarios revisar periódicamente este documento para estar al tanto de cualquier cambio que pueda ocurrir. En caso de realizar cambios significativos, de acuerdo con la legislación aplicable, se informará a los Usuarios con anticipación sobre dichos cambios.  <br>

    <br><h5>9. Política de tratamiento de información aplicable</h5>
